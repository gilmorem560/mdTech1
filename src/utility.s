/* ------------------------------------------------------------------------- */
/* utility - useful subroutines                                              */
/* ------------------------------------------------------------------------- */
	.global	nullloop
	.global	nullint
	.global	nullsub
/* ------------------------------------------------------------------------- */
	.text
/* ------------------------------------------------------------------------- */

/* ------------------------------------------------------------------------- */
/* nullloop - empty loop                                                     */
/* ------------------------------------------------------------------------- */
nullloop:
	nop
	nop
	bra.s	nullloop
	
/* ------------------------------------------------------------------------- */
/* nullint - empty interrupt                                                 */
/* ------------------------------------------------------------------------- */
nullint:
	nop
	nop
	rte
	
/* ------------------------------------------------------------------------- */
/* nullsub - empty subroutine                                                */
/* ------------------------------------------------------------------------- */
nullsub:
	nop
	nop
	rts
